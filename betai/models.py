def calc_edge(k_dec: float, p_model: float) -> float:
    """
    Рассчитывает edge (преимущество) ставки на основе десятичного коэффициента
    букмекера и вероятности исхода по нашей модели.
    
    Args:
        k_dec (float): Десятичный коэффициент букмекера (например, 2.5)
        p_model (float): Вероятность исхода по нашей модели (0-1)
        
    Returns:
        float: Значение edge. Положительное значение означает выгодную ставку.
    
    Examples:
        >>> calc_edge(2.0, 0.6)  # Букмекер дает 2.0, наша модель оценивает вероятность как 60%
        0.2  # Edge = 20%
        
        >>> calc_edge(1.5, 0.5)  # Букмекер дает 1.5, наша модель оценивает вероятность как 50%
        -0.25  # Edge = -25% (невыгодная ставка)
    """
    return (k_dec * p_model) - 1
